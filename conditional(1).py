temperature = 99
is_raining = False

if temperature > 30:
    print("It's a hot day!")
elif temperature >= 20:
    print("It's a pleasant day.")
else:
    print("It's a bit chilly.")

if is_raining:
    print("Remember your umbrella.")
else:
    print("No need for an umbrella today.")

